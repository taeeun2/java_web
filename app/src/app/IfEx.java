package app;

/*
 * 제어문(조건문)
 * 
 * if, else의 조건식에 따라 프로그램의 흐름을 변경한다.
 * 표현)
 * 	if(조건식){
 * 		실행 구문 1;
 * 	}
 * 	else{
 * 		실행 구문 2;
 * 	}
 */
public class IfEx {

	public static void main(String[] args) {
		int num = 10;
		if(num==10) 
			System.out.println("참");
		else
			System.out.println("거짓");

		
		
	}

}
