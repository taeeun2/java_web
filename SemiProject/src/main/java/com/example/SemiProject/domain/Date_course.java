package com.example.SemiProject.domain;

public class Date_course {
	private String user_place_id;
	private String place_name;
	private String place_address;
	private int sequence;
	private String img;
	private String comment;
	public String getUser_place_id() {
		return user_place_id;
	}
	public void setUser_place_id(String user_place_id) {
		this.user_place_id = user_place_id;
	}
	public String getPlace_name() {
		return place_name;
	}
	public void setPlace_name(String place_name) {
		this.place_name = place_name;
	}
	public String getPlace_address() {
		return place_address;
	}
	public void setPlace_address(String place_address) {
		this.place_address = place_address;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	
	
}
